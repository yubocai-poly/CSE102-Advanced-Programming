def maze(m, n, i, j):
    if i == n - 1 and j == n - 1:
        return [(n - 1, n - 1)]

    position = [(i, j + 1), (i + 1, j)]

    for new_i, new_j in position:
        if new_i >= n or new_j >= n:
            continue
        if m[new_i][new_j] == 0:
            continue
        path = maze(m, n, new_i, new_j)
        if path is not None:
            return [(i, j)] + path


